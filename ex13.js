// Encontrar temperatura ideal
// *Usando Operadores lógicos, Ternário, If e Else


const temperatura = 37

if(temperatura <= 35) {console.log("Está com hipotermia")}

else if(temperatura >= 36 && temperatura <= 37.5) {console.log("Está saudável")}

else if(temperatura >= 37.6 && temperatura <=38) {console.log("Está febril")}

else if(temperatura >= 39) {console.log("Está com febre")}

else{console.log("Resultado não encontrado, digite novamente")}


